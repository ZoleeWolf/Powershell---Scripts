try {
    $word = New-Object -ComObject Word.Application
    $normalTemplate = $word.NormalTemplate.OpenAsDocument()
    if ($normalTemplate.CompatibilityMode -lt 15) {
        # Backup before conversion
        $storePath = $word.Options.DefaultFilePath([Microsoft.Office.Interop.Word.WdDefaultFilePath]::wdUserTemplatesPath) + "\backup_before_conversion\"
        if (-not (Test-Path -Path $storePath)) {
            New-Item -ItemType Directory -Path $storePath
        }
        
        $timestamp = Get-Date -Format "yyyy-MM-dd_HH-mm-ss"
        $backupName = $storePath + "Normal_Backup_$timestamp.dotm"
        $normalTemplate.SaveAs($backupName)
        
        # Convert to Word 365 format
        $normalTemplate.Convert()
        $normalTemplate.Save()
    }
    $normalTemplate.Close()
    $word.Quit()
    }
catch {
    Write-Error "An error occurred: $_"
    if ($normalTemplate -and $normalTemplate.Saved -eq $false) {
        $normalTemplate.Close()
    }
    if ($word) {
        $word.Quit()
    }
}

